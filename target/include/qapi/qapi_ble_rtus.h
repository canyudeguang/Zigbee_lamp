/**
 * Enumeration that represents all of the valid commands that may be
 * received in an
 * QAPI_BLE_ET_RTUS_SERVER_TIME_UPDATE_CONTROL_POINT_COMMAND_E server
 * event or that may be written to a remote RTUS server.
 */
typedef enum
{
   QAPI_BLE_CP_GET_REFERENCE_UPDATE_E    = QAPI_BLE_RTUS_TIME_UPDATE_CONTROL_POINT_GET_REFERENCE_UPDATE,
   /**< Get reference update command. */
   QAPI_BLE_CP_CANCEL_REFERENCE_UPDATE_E = QAPI_BLE_RTUS_TIME_UPDATE_CONTROL_POINT_CANCEL_REFERENCE_UPDATE
   /**< Cancel reference update command. */
} qapi_BLE_RTUS_Time_Update_Control_Command_t;

/**
 * Enumeration that represents all the events generated by the RTUS
 * service. These are used to determine the type of each event
 * generated, and to ensure the proper union element is accessed for the
 * #qapi_BLE_RTUS_Event_Data_t structure.
 */
typedef enum
{
   QAPI_BLE_ET_RTUS_SERVER_TIME_UPDATE_CONTROL_POINT_COMMAND_E
   /**< Write Time update control point request event. */
} qapi_BLE_RTUS_Event_Type_t;

/**
 * Structure that represents the format for the data that is dispatched
 * to an RTUS server when an RTUS client has sent a request to write the
 * RTUS Time Update Control Point.
 */
typedef struct qapi_BLE_RTUS_Time_Update_Control_Command_Data_s
{
   /**
    * RTUS instance that dispatched the event.
    */
   uint32_t                                    InstanceID;

   /**
    * GATT connection ID for the connection with the RTUS client
    * that made the request.
    */
   uint32_t                                    ConnectionID;

   /**
    * GATT connection type, which identifies the transport used for
    * the connection with the RTUS client.
    */
   qapi_BLE_GATT_Connection_Type_t             ConnectionType;

   /**
    * Bluetooth address of the RTUS client that made the request.
    */
   qapi_BLE_BD_ADDR_t                          RemoteDevice;

   /**
    * RTUS Time Update Control Point command that has been requested
    * to be written.
    */
   qapi_BLE_RTUS_Time_Update_Control_Command_t Command;
} qapi_BLE_RTUS_Time_Update_Control_Command_Data_t;

#define QAPI_BLE_RTUS_TIME_UPDATE_CONTROL_COMMAND_DATA_SIZE  (sizeof(qapi_BLE_RTUS_Time_Update_Control_Command_Data_t))
